# Desc: Ask the user for 2 numbers and see which one is bigger and which one is smaller.


first_number = input('Enter your first number:')
first_number = float(first_number)
second_number = input('Enter your second number:')
second_number = float(second_number)
if first_number > second_number:
    print('The bigger number is',first_number)
    print('The smaller number is',second_number)
elif first_number == second_number:
    print('Both numbers have the same value')
else:
    print('The bigger number is',second_number)
    print('The smaller number is',first_number)






                     







